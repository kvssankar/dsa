
#include <bits/stdc++.h>
using namespace std;

//this can find minmum only it cant find pivot for duplicates
class Solution {
public:
    int findMin(vector<int>& arr) {
        int n=arr.size();
        int l=0;
        int r=n-1;
        int ans=INT_MAX;
        while(l<=r){
            int m=l+(r-l)/2;
            if(arr[l]<=arr[m]){
                ans=min(ans,arr[l]);
                l=m+1;
            }else{
                ans = min(ans, arr[m]);
                r=m-1;
            }
        }
        return ans;
    }
};


bool searchInARotatedSortedArrayII(vector<int>&arr, int k) {
    int n = arr.size(); // size of the array.
    int low = 0, high = n - 1;
    while (low <= high) {
        int mid = (low + high) / 2;

        //if mid points the target
        if (arr[mid] == k) return true;

        //Edge case:
        if (arr[low] == arr[mid] && arr[mid] == arr[high]) {
            low = low + 1;
            high = high - 1;
            continue;
        }

        //if left part is sorted:
        if (arr[low] <= arr[mid]) {
            if (arr[low] <= k && k <= arr[mid]) {
                //element exists:
                high = mid - 1;
            }
            else {
                //element does not exist:
                low = mid + 1;
            }
        }
        else { //if right part is sorted:
            if (arr[mid] <= k && k <= arr[high]) {
                //element exists:
                low = mid + 1;
            }
            else {
                //element does not exist:
                high = mid - 1;
            }
        }
    }
    return false;
}

int main()
{
    vector<int> arr = {7, 8, 1, 2, 3, 3, 3, 4, 5, 6};
    int k = 3;
    bool ans = searchInARotatedSortedArrayII(arr, k);
    if (!ans)
        cout << "Target is not present.\n";
    else
        cout << "Target is present in the array.\n";
    return 0;
}

